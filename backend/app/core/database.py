from sqlalchemy import create_engine
from sqlalchemy.orm import DeclarativeBase, sessionmaker

from app.core.config import settings


# -----------------------------------------------------------------------------
# Engine — керує пулом з'єднань до PostgreSQL
# -----------------------------------------------------------------------------
engine = create_engine(
    settings.DATABASE_URL,
    # echo=True — виводить у консоль усі SQL-запити, зручно для навчання.
    # На продакшені треба вимкнути (великі логи).
    echo=settings.DEBUG,
    # Розмір пулу: 5 постійних з'єднань + ще 10 за потребою.
    pool_size=5,
    max_overflow=10,
    # pool_pre_ping=True — перед видачею з'єднання перевіряє, що воно живе.
    # Рятує від помилок, коли БД перезапустилася, а пул не знає.
    pool_pre_ping=True,
)


# -----------------------------------------------------------------------------
# SessionLocal — "фабрика" сесій.
# Кожен виклик SessionLocal() створює нову сесію.
# -----------------------------------------------------------------------------
SessionLocal = sessionmaker(
    autocommit=False,   # Не комітити автоматично — контроль за нами
    autoflush=False,    # Не відправляти зміни в БД автоматично
    bind=engine,
)


# -----------------------------------------------------------------------------
# Base — базовий клас для всіх наших моделей (User, Genre, Track...).
# Успадкування від нього дає магію ORM.
# -----------------------------------------------------------------------------
class Base(DeclarativeBase):
    pass


# -----------------------------------------------------------------------------
# get_db — залежність для FastAPI.
# Кожен ендпоінт, який хоче працювати з БД, буде отримувати сесію через неї.
# -----------------------------------------------------------------------------
def get_db():
    """
    Створює нову сесію на кожен HTTP-запит.
    Автоматично закриває її після завершення (навіть якщо сталася помилка).
    
    Використання у ендпоінті:
        def my_endpoint(db: Session = Depends(get_db)):
            ...
    """
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()